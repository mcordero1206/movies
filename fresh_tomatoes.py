"""
One single film entry html template.
"""
movie_tile_content = '''
<div class="col-md-6 col-lg-4 movie-tile text-center" data-trailer-youtube-id="{trailer_youtube_id}" data-toggle="modal" data-target="#trailer">
    <img src="{poster_image_url}" width="220" height="342">
    <h2>{movie_title}</h2>
    <p>{duration_in_minutes} minutes</p>
    <p>{movie_storyline}</p>
</div>
'''

"""
This function has the HTML
content for one section of
the webpage. It pulls up the
youtube ID from the url and
appends the film title for 
the paticular movie with 
it's content filled in.
"""
def create_movie_tiles_content(movies):
    content = ''
    for movie in movies:
        youtube_id_match = re.search(
            r'(?<=v=)[^&#]+', movie.trailer_youtube_url)
        youtube_id_match = youtube_id_match or re.search(
            r'(?<=be/)[^&#]+', movie.trailer_youtube_url)
        trailer_youtube_id = (youtube_id_match.group(0) if youtube_id_match
                              else None)

        content += movie_tile_content.format(
            movie_title=movie.title,
            poster_image_url=movie.poster_image_url,
            trailer_youtube_id=trailer_youtube_id,
            movie_storyline=movie.storyline,
            duration_in_minutes=movie.duration_in_minutes
        )
    return content

"""
This function pulls up the
output file or is replaced
with some titles with generic
default content. Opens the
output file on a new tab in
the user's web browser.
"""
def open_movies_page(movies):
    output_file = open('fresh_tomatoes.html', 'w')

    rendered_content = main_page_content.format(
        movie_tiles=create_movie_tiles_content(movies))

    output_file.write(main_page_head + rendered_content)
    output_file.close()

    url = os.path.abspath(output_file.name)
    webbrowser.open('file://' + url, new=2)